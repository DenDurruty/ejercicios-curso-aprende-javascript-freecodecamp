/*
- == es un operador de igualdad
- === es un operador de igualdad estricta, determina si el tipo de dato es el mismo

*/

// Operador de igualdad
console.log(9 == 9); // true
console.log(9 == "9"); // true // antes de realizar la operaci√≥n ambos valores se convierten al mismo tipo de dato

// Operador de igualdad estricta
console.log(9 === 9); // true
console.log(9 === "9"); // false



